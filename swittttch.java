public class swittttch {
    public static void main(String[] args) {
        int n = 9;
        switch (n) {
            case 5:
                System.out.println("It is five");
                break;
            case 7:
                System.out.println("It is seven");
            case 8:
                System.out.println("It is eight");
            default:
            System.out.println("Just a normal number : "+n);
                break;
        }
    }
}
